// ── Business SKILL.md builder ─────────────────────────────────────────────────
function buildBusinessSKILLmd(biz: BusinessSkillPackage): { text: string; lineMap: Record<string, number> } {
  const lines: string[] = []
  const lineMap: Record<string, number> = {}
  const push = (...ls: string[]) => { lines.push(...ls) }
  const mark = (key: string) => { lineMap[key] = lines.length + 1 }

  // Frontmatter (generated from structured fields)
  mark('biz-basic')
  mark('biz-triggers')
  mark('biz-prerequisites')
  push(`---`, `name: ${biz.name}`, `description: ${biz.description}`, `version: ${biz.version}`, `---`, ``)

  // Body (freely editable markdown)
  mark('biz-skillbody')
  const bodyLines = biz.skillBodyMd.split('\n')
  bodyLines.forEach(l => push(l))

  return { text: lines.join('\n'), lineMap }
}

