// ── Business section: Triggers ────────────────────────────────────────────────
function BizTriggersSection({ biz, setBiz, onFocus }: BizSectionProps) {
  const [inputVal, setInputVal] = useState('')

  const addTrigger = useCallback((val: string) => {
    const trimmed = val.trim().replace(/,$/, '').trim()
    if (trimmed && !biz.triggers.includes(trimmed)) {
      setBiz(b => ({ ...b, triggers: [...b.triggers, trimmed] }))
    }
    setInputVal('')
  }, [biz.triggers, setBiz])

  const handleKeyDown = (e: React.KeyboardEvent<HTMLInputElement>) => {
    if (e.key === 'Enter' || e.key === ',') {
      e.preventDefault()
      addTrigger(inputVal)
    } else if (e.key === 'Backspace' && inputVal === '' && biz.triggers.length > 0) {
      setBiz(b => ({ ...b, triggers: b.triggers.slice(0, -1) }))
    }
  }

  const removeTrigger = (t: string) => {
    setBiz(b => ({ ...b, triggers: b.triggers.filter(x => x !== t) }))
  }

  return (
    <div id="biz-triggers" onFocus={() => onFocus('biz-triggers')}>
      <SectionHeading>触发词</SectionHeading>
      <CardBox>
        <div style={{ marginBottom: '10px', fontSize: '12px', color: 'var(--muted)' }}>
          {biz.triggers.length} 个关键词 · 按 Enter 或逗号添加
        </div>
        <div style={{
          display: 'flex', flexWrap: 'wrap', gap: '6px', alignItems: 'center',
          border: '1px solid var(--hairline)', borderRadius: '10px', padding: '8px 10px',
          background: 'white', minHeight: '44px',
        }}>
          {biz.triggers.map(t => (
            <span key={t} style={{
              display: 'flex', alignItems: 'center', gap: '4px', padding: '3px 10px',
              borderRadius: '99px', background: 'var(--surface-card)', fontSize: '12px', color: 'var(--ink)',
            }}>
              {t}
              <button onClick={() => removeTrigger(t)} style={{ background: 'none', border: 'none', cursor: 'pointer', color: 'var(--muted-soft)', fontSize: '13px', lineHeight: 1, padding: '0 0 0 2px' }}>✕</button>
            </span>
          ))}
          <input
            type="text"
            value={inputVal}
            onChange={e => setInputVal(e.target.value)}
            onKeyDown={handleKeyDown}
            onBlur={() => inputVal.trim() && addTrigger(inputVal)}
            placeholder={biz.triggers.length === 0 ? '输入关键词...' : ''}
            style={{ border: 'none', outline: 'none', fontSize: '13px', background: 'transparent', flexGrow: 1, minWidth: '80px', color: 'var(--ink)' }}
          />
        </div>
      </CardBox>
    </div>
  )
}

